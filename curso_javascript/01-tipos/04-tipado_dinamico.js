/*
JavaScript es de tipado dinámico, esto nos permite que aunque una varible sea incializada con un tipo de dato 
proximamente en el codigo se le puede cambiar no solo el valor sino tambien el tipo de dato almacenado
*/
let numero = 42;
let nombre = "Adrian";
let verdadero = true;
let undef;
let nula = null;

//La función "typeof" me retorna el tipo de dato de la variable
console.log(typeof numero);
console.log(typeof nombre);
console.log(typeof verdadero);
console.log(typeof undef);
console.log(typeof nula);
