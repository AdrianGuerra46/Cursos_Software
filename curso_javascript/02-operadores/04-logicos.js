// Operador AND, ambas deben ser verdaderas para retornar true

let mayor = true;
let suscrito = true;

console.log('operador and', mayor && suscrito);


// Operador OR, una de las dos debe ser verdadera para retornar true

console.log('operador or', mayor || suscrito);

// Operador NOT, invierte, true a false y false a true

console.log('operador not', !mayor);

