let a = 5;
let b = 7;

// Como vemos a continuación el console.log permite concatenar 

console.log(a + b, "Adición"); // Adición
console.log(a - b, "Resta"); // Resta
console.log(a * b, "Multtpliación"); // Multiplicación
console.log(a / b, "Divisón"); // División
console.log(a % b, "Módulo"); // Módulo 
console.log(a ** b, "Potencia"); // Potencia

//Incrementar 
console.log(++a);
console.log(a++);
console.log(a);
//Decrementar 
console.log(--a);
console.log(a--);
console.log(a);

