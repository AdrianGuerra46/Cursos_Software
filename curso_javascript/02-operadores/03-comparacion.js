let a = 10;

//*Operadores Relacionales
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

//*Operadores de igualdad

console.log(a == 10); //?Este tipo de igualdad solo "pregunta" valor NO tipo de dato
console.log(a != 10);
console.log(a === '10'); //? Este tipo de igualdad "preguntja" valor Y tipo de dato. Son los mas recomendables
console.log(a !== '10');
